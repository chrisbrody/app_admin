    <!-- Step By Step for Class Time -->
    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week9Step1">Chit chat - Ask the question to each student - what is the one thing they saw that had the most potential in jQuery.  Also answer any questions students have on jQuery, often asking simple questions like how do you include jQuery or how do you select element</li>
        <li id="Week9Step2">
          Start by telling students they should be thinking about ways to implement jQuery into their app - Discuss when we would choose to use jQuery in a project, as well as the potential jQuery has. - here is a list of things to talk about and show examples, <b>all are very useful in the jQuery world:</b>
          <ul>
            <li>How to include jQuery - direct download / jQuery cdn /google cdn</li>
            <li>document.ready v document.load</li>
            <li>how to use compound element selectors so you remain DRY</li>
            <li>events: mouseenter/mouseleave/click/dlbclick/hover</li>
            <li>fade In/Out/Toggle, slide In/Out/Toggle, show/hide</li>
            <li>css() v .animate()</li>
            <li>addClass and removeClass</li>
            <li>append/prepend vs after/before</li>
            <li>.val() - key for getting input data!</li>
          </ul>
        </li>
        <li id="Week9Step3">Show off <a href="http://jqueryrain.com/" target="_blank">jQuery Rain Plugins</a> & <a href="http://plugins.jquery.com/" target="_blank">jQuery Plugins</a> - explain how to use, download & include a plugin into a project. Take a few minutes to show some of your favorite plugins and how you have used them in the past</li>
        <li id="Week9Step4">Introduce php server - <a href="https://www.apachefriends.org/index.html" target="_blank">Xampp</a> - have each student download it, and explain how to use it - saving files in htdocs & how to access these files localhost/folderName</li>
        <li id="Week9Step5">Introduce php includes <code>&lt;? php include(“fileName.path”) ?&gt; </code> - explain how it can be utilized in coding, how it can improve the amount of time you spend searching for elements you have created for a project.</li>
        <li id="Week9Step6">Give each group 5 - 10 minutes to present their app projects progress - where they are currently and then provide feedback and constructive criticism, prompt students to do the same</li>
        <li id="Week9Step7">Break back into project groups and continue to work on App Project </li>
      </ol>

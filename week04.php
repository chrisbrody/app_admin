      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week4Step1">Time: 5-10 mins - Chit chat - about the past week - also talk about their experiences so far - they have learned a lot about html &amp; css</li>
        <li id="Week4Step2">Introduce students to and get them using <a href="http://daneden.github.io/animate.css/" target="">CSS Animations</a> - talk about how and why it can be useful</li>
        <li id="Week4Step3">Talk about how many users are on mobile devices -70% of users and growing  so making them responsive is very important. Thi can be achieved using media queries - basic ex. Walk them through and have them test out the media query example on w3schools.com. Be sure to change the size and add css rules. <a href="http://www.w3schools.com/cssref/css3_pr_mediaquery.asp" target="">w3schools media queries</a></li>
        <li id="Week4Step4">Review <a href="http://www.w3schools.com/cssref/css3_pr_mediaquery.asp" target="">media queries</a> using w3schools and do some basic testing on a blank project before moving on to the weekly project</li>
        <li id="Week4Step5">Show them the resizing tools available in the chrome and firefox dev-tools</li>
        <li id="Week4Step6">Begin making the Google Replication project responsive using media queries - start by having everyone reduce the size of their browser window</li>
        <li id="Week4Step7">15 mins before the end of class - talk about <a href="http://codifyacademy.com/bootstrap3/" target="">Bootstrap 3</a>(mainly how it is a library of CSS class we call to get a desired effect) and what is expected of this week - (completing all tasks to move on) - show them where to download the <a href="http://www.initializr.com/" target="">HTML5 Bootstrap Boilerplate</a></li>
      </ol>

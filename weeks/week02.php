    <!-- Step By Step for Class Time -->
    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week2Step1">
          Start with Cartoon Character Introductions - 
          <ul>
            <li><b>Instructions:</b> Everyone introduces themselves to one other person as the cartoon character they would be if they were not who they are.</li>
            <li><b>Desired outcome:</b> Creates a relaxed atmosphere, helps everyone break out of their shell and get more comfortable as a team</li>
          </ul>
                       
        </li>
        <li id="Week2Step2">Review the <a href="http://www.w3schools.com/css/css_boxmodel.asp" target="_blank">Box Model</a> and how each property can affect other elements
          <ul>
            <li>content</li>
            <li>padding</li>
            <li>border</li>
            <li>margin</li>
          </ul> 
        </li>
        <li id="Week2Step3">Introduce them to <a href="http://codifyacademy.com/salespage/" target="_blank">37Signals Sales Page</a> &amp; the concept of Paper Prototyping - drawing and defining HTML tags and CSS properties to be used on this project.
          <ul>
            <li>Start with the nav, draw it out, define all tags and css properties that will be used to create the section</li>
            <li>Draw out the Header again defining HTML tags and CSS properties for each element</li>
            <li>Next do Products section paper prototype</li>
            <li>Next do Customers section paper prototype</li>
            <li>Next do Manifesto section paper prototype</li>
            <li>Next do Rework section paper prototype</li>
            <li>Next do Rework section paper prototype</li>
            <li>Finish with Footer if there is time</li>
          </ul>
        </li>

        <span id="introduceThese">Introduce these as the students ask questions about them</span>

        <li id="Week2Step4">Talk about the <a href="http://www.w3schools.com/cssref/pr_class_position.asp" target="_blank">Position Property</a> and how to use it &amp; have every student find each using w3schools
          <ul>
            <li>Fixed</li>
            <li>Absolute</li>
            <li>Relative</li>
          </ul>
        </li>
        <li id="Week2Step5">Introduce new Tags - talk about how the are useful &amp; have every student find each using w3schools
          <ul>
            <li><a href="http://www.w3schools.com/tags/tag_header.asp" target="_blank">header</a></li>
            <li><a href="http://www.w3schools.com/tags/tag_footer.asp" target="_blank">footer</a></li>  
            <li><a href="http://www.w3schools.com/tags/tag_nav.asp" target="_blank">nav</a></li>  
            <li><a href="http://www.w3schools.com/tags/tag_section.asp" target="_blank">section</a></li>
          </ul>
        </li>
        <li id="Week2Step6">Introduce sprites: images originally used by video game developers to give characters more movement options without taking up a lot of memory. People use it now to decrease the size of the website, which will speed it up a bit. They’re not widely used, but you will come across it in the future</li>
        <li id="Week2Step7">Introduce the sup &amp; sub tags - have students search these using w3schools
          <ul>
            <li><a href="http://www.w3schools.com/tags/tag_sup.asp" target="_blank">&lt;sup&gt;</a> tag defines superscript, which will make the text it is wrapped around appear half a character above the normal line, and is sometimes rendered in a smaller font</li>
            <li><a href="http://www.w3schools.com/tags/tag_sub.asp" target="_blank">&lt;sub&gt;</a> tag defines subscript text. Subscript text appears half a character below the normal line, and is sometimes rendered in a smaller font. Subscript text can be used for chemical formulas, like H2O</li>
          </ul>
        <li id="Week2Step8">Symbols in html are better than writing copying and pasting the characters. Some browsers will not recognize a character that is copied like this, which is why using symbols is always better. Her is a list of the symbols along with the code you’ll need to display them, <a href="http://www.ascii.cl/htmlcodes.htm" target="_blank">ascii codes</a> - share this link and talk about how it can be useful</li>
        <li id="Week2Step9">Google Fonts - and how to use them in any project
          <ul>
            <li>go to <a href="http://www.google.com/fonts/" target="_blank">google.com/fonts</a></li>
            <li>Find the font you want to use and click the “quick use” button</li>
            <li>Choose the styling of the font you want, adding more stylings will slow down the page load time, so the less the better</li>
            <li>Scroll down to the last 2 sections</li>
            <li>Copy and paste the link tag into the &lt;head&gt; tags of your web page</li>
            <li>Lastly, add the font-family to the css exactly like they show you, save all and refresh</li>
          </ul>
        </li>
        <li id="Week2Step10">
          -webkit, -o and -moz, intro to compatibility testing - <a href="http://pleeease.io/play/" target="_blank">CSS Prefixer</a> - talk briefly about how you should drop your code in the css prefixer when you’re finished with your css page - also mention:
            <ul>
              <li>Every company’s customers are different. So which browsers and devices you test should be determined by which ones the customers use the most. Compatibility takes a long time, if only 1% of your customers use IE, then spending 20% of your time trying to make it work there doesn’t make a lot of sense</li>
              <li>The same is true for resolution size or screen size. If 90% of your customers use apple phones, then you should focus on those, instead of other devices. Lucky for us, google analytics provides all of the information you would nee to know about your customers</li>
            </ul>
        </li>
        <li id="Week2Step11">
          How to center divs and images
          <ul>
            <li>using <code>margin: 0 auto;</code> and providing the div with a <code>width</code>. Remember that the parent element must have a defined width in order for it to calculate the margin</li>
            <li>alternatively you can use <code>text-align: center;</code> on the <b>parent div</b> to center anything inside</li>
          </ul>
        </li>
      </ol>

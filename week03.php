    <!-- Step By Step for Class Time -->
    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week3Step1">Chit chat - 10 - 15 mins about what they found to be the most interesting thing they came across in coding this week</li>
        <li id="Week3Step2">Cover centering divs and images using margin: 0 auto; and providing the div with a width. Remember that the parent element must have a defined width in order for it to calculate the margin</li>
        <li id="Week3Step3">
          <a href="https://html5boilerplate.com/" target="_blank">HTML5 Boilerplate</a> makes our lives easier - when we want to start a project this is where we start - take them through and explain each line and what its used for
          <br>
          <b>SKIP - the js stuff at the bottom</b>
        </li>
        <li id="Week3Step4">Download colorpick eyedropper and page ruler in chrome extension, download colorzilla and measurelt in the add-ons if you have firefox</li>
        <li id="Week3Step5">Use the new extensions for 3 - 5 mins on any website - to get acquainted with them</li>
        <li id="Week3Step6">
          Take some time to introduce students to <a href="https://www.canva.com/" target="_blank">Canva</a>
          <ul>
            <li>Have everyone create a user login</li>
            <li>Show some of our canva work, and talk about how you went about creating your canva projects</li>
            <li>Walkthrough how to create canva projects - searching elements - loading pictures ect</li>

          </ul>
        </li>        
        <li id="Week3Step7">
          Discuss the importance of PSD’s in the industry
          <ul>
            <li>It’s very common for developers to not be good at design and designers to not be good at coding. So most companies hire a designer and developer to work together. The designer will design a page and send the photoshop files to the developer. When that happens, you won’t have access to source code or be able to inspect the css in the dev tools. So you’ll need to get good at coding from scratch in order to find a job</li>
          </ul>
        </li>
        <li id="Week3Step8">In google, type in “photoshop website”, click on the images tab, and choose of of these for your PSD replica projects. Choose something that’s challenging, but not unlikely for you to finish</li>
        <li id="Week3Step9">Paper prototype out the psd that they’re going to be replicating ( remind everyone of the importance of this step )</li>
        <li id="Week3Step10">Once the paper prototype is done have the students begin coding</li>
      </ol>

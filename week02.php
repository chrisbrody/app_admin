    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week2Step1">
          Start with Cartoon Character Introductions - <b>Instructions:</b> Everyone introduces themselves to one other person as the cartoon character they would be if they were not who they are. <b>Desired outcome:</b> Creates a relaxed atmosphere            
        </li>
        <li id="Week2Step2">Review the <a href="http://www.w3schools.com/css/css_boxmodel.asp" target="_blank">Box Model</a> - content, padding, border, margin</li>
        <li id="Week2Step3">Talk about the <a href="http://www.w3schools.com/cssref/pr_class_position.asp" target="_blank">Position Property</a> - Fixed / Absolute / Relative </li>
        <li id="Week2Step4">
          Introduce new Tags - 
          <a href="http://www.w3schools.com/tags/tag_header.asp" target="_blank">header</a> / 
          <a href="http://www.w3schools.com/tags/tag_footer.asp" target="_blank">footer</a> / 
          <a href="http://www.w3schools.com/tags/tag_nav.asp" target="_blank">nav</a> / 
          <a href="http://www.w3schools.com/tags/tag_section.asp" target="_blank">section</a>
        </li>
        <li id="Week2Step5">Introduce sprites: images originally used by video game developers to give characters more movement options without taking up a lot of memory. People use it now to decrease the size of the website, which will speed it up a bit. They’re not widely used, but you will come across it in the future</li>
        <li id="Week2Step6">Introduce what the <a href="http://www.w3schools.com/tags/tag_sup.asp" target="_blank">&lt;sup&gt;</a> tag stands for, superscript, which will make the text it is wrapped around appear half a character above the normal line, and is sometimes rendered in a smaller font and what the <a href="http://www.w3schools.com/tags/tag_sub.asp" target="_blank">&lt;sub&gt;</a> tag does</li>
        <li id="Week2Step7">Symbols in html are better than writing copying and pasting the characters. Some browsers will not recognize a character that is copied like this, which is why using symbols is always better. Her is a list of the symbols along with the code you’ll need to display them, <a href="http://www.ascii.cl/htmlcodes.htm" target="_blank">ascii codes</a></li>
        <li id="Week2Step8">
          Introduce Google Fonts
          <ul>
            <li>go to <a href="http://www.google.com/fonts/" target="_blank">google.com/fonts</a></li>
            <li>Find the font you want to use and click the “quick use” button</li>
            <li>Choose the styling of the font you want, adding more stylings will slow down the page load time, so the less the better</li>
            <li>Scroll down to the last 2 sections</li>
            <li>Copy and paste the link tag into the &lt;head&gt; tags of your web page</li>
            <li>Lastly, add the font-family to the css exactly like they show you</li>
          </ul>
        </li>
        <li id="Week2Step9">
          webkit and moz, intro to compatibility testing - <a href="http://pleeease.io/play/" target="_blank">CSS Prefixer</a>
            <ul>
              <li>Every company’s customers are different. So which browsers and devices you test should be determined by which ones the customers use the most. Compatibility takes a long time, if only 1% of your customers use IE, then spending 20% of your time trying to make it work there doesn’t make a lot of sense. The same is true for resolution size or screen size. If 90% of your customers use apple phones, then you should focus on those, instead of other devices. Lucky for us, google analytics provides all of the information you would need to know about your customers</li>
            </ul>
        </li>
        <li id="Week2Step10">Introduce them to <a href="http://codifyacademy.com/salespage/" target="_blank">37Signals Sales Page</a> &amp; the concept of Paper Prototyping section 1 and define all tags and css properties that can be used to create the section</li>
        <li id="Week2Step11">Finish the session Paper Prototyping / Coding it up -  1 section at a time - till class ends</li>
      </ol>

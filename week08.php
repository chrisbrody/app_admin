    <!-- Step By Step for Class Time -->
    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week8Step1">Chit chat - and play <a href="http://multeor.com/about/" target="">Multeor</a> - if you play 3 games - 2 groups play a round, take the top 2 high scorers from each group and play a final match with top 4 players</li>
        <li id="Week8Step2">Take 5 - 10 mins for each student to present their finished Themes - offer praise and constructive feedback for the project</li>
        <li id="Week8Step3">
            Review JS topics covered this week and the past week - create at least 2 examples of each
            <ul>              
              <li>variables - strings | numbers | booleans</li>
              <li>arrays</li>
              <li>functions</li>
              <li>if / else</li>
              <li>for loops</li>
              <li>while loops</li>
              <li>objects</li>
              <li>case / switch</li>
            </ul>
        </li>
        <li id="Week8Step4">Showcase <a href="http://firebase.com/" target="">Firebase</a> - take students through the simple step by step process in browser on how to access | save | update | delete info. After the walk through have the students create a new boilerplate and have them create a firebase login to send data to firebase and save JavaScript objects to it</li>
        <li id="Week8Step5">Select teams for app/website projects - use your discretion to select the teams - ideally 2 students per team</li>
        <li id="Week8Step6">Review Application Project  specs and discuss how to tackle the project - step by step lists are a great way to start</li>
        <li id="Week8Step7">Begin brainstorming ideas for project - allow the groups to split off for 30 - 45 mins paper prototyping</li>
        <li id="Week8Step8">Return back to the room to critique the  paper prototyping - give each group 5 mins to express their idea for their new creation / present & critique paper prototypes</li>
        <li id="Week8Step9">15 mins before the end of class - Have each group decide who will do what during the week </li>
      </ol>

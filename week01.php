    <!-- Step By Step for Class Time -->
    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week1Step1">
          Chit chat - quick intro - (about yourself) + Mention, at anytime, if you have a question please interrupt.
          <br>
          <b>Team building questions</b> - Give everyone some time to answer each
          <ul>
            <li>Why are you learning to code?</li>
            <li>What is your current dream in life you're trying to achieve? ( besides learning to code &amp; getting a new job )</li>
            <li>What is something about yourself you haven't told anyone in this room yet?</li>
          </ul>
        </li>
        <li id="Week1Step2">
          Talk about the coding process - 20% coding, 80% debugging. 99.99% of the time, it is user error. Coding is extremely frustrating, and Google is your best friend. Resources are key. 
        </li>
        <li id="Week1Step3">
          Talk about difference between front end and back end - and how they work together
          <br>
          <span>Example:</span> Facebook - What you see on the screen is created by the front end developer - you put in your information to sign up which is stored in a  databases which is created by the back end developer. How this information is displayed in the future is done by the front end developer.
        </li>
        <li id="Week1Step4">
          How the web browser communicates with the server to display any HTML page Using <a href="TheBigPicture.pdf" download>TheBigPicture.pdf Download Here</a>
        </li>
        <li id="Week1Step5">
          Discuss important things to know when starting coding- now is also a good time to list the most common tags:- show how you create and add content to each. Discuss the most common  CSS properties how they work have students use <a href="http://www.w3schools.com/html/" target="_blank">HTML5 w3schools</a> to see how each works
          <ul>
            <h5>HTML5</h5>
            <li>h1 - h6 - block</li>
            <li>p - block</li>
            <li>img - inline</li>
            <li>a - inline</li>
            <li>section - block</li>
            <li>article - block</li>
            <li>div - block</li>
            <li>span - inline</li>
            <li>input - inline</li>
            <h5>CSS</h5>
            <li>width</li>
            <li>height</li>
            <li>margin</li>
            <li>padding</li>
            <li>color</li>
            <li>background-color</li>
            <li>border</li>
            <li>display</li>

          </ul>
        </li>
        <li id="Week1Step6">
          Review 3 different ways to include CSS - inline, &lt;style&gt; and .css file
        </li>
        <li id="Week1Step7">
          Have students create a new folder on their desktop called Google Start a new file in notepad/textedit save it as index.html - also create an img folder &amp; css folder - to be used late
          <ul>
            <li>add h1 tag with some content from <a href="http://www.lipsum.com/" target="_blank">Lorem Ipsum</a>, or the generic hello world</li>
            <li>Add p tag with some style { color: red; } to notepad 3 different ways, in head, in-line, in main.css and add some content</li>
          </ul>
        </li>
        <li id="Week1Step8">
          Talk about <a href="http://www.w3schools.com/css/css_boxmodel.asp" target="_blank">Box Model</a> - and have everyone try it out using inspect element - have them add padding and margin values to the H1 and P tags to try to grasp what is going on
        </li>
        <li id="Week1Step9">
          Have each student use inspect element on favorite website &amp; change HTML and CSS in browser
        </li>
        <li id="Week1Step10">
          Show them <a href="http://www.litmos.com/" target="_blank">Litmos</a> and how to view projects
        </li>
        <li id="Week1Step11">
          Talk about <a href="http://www.codecademy.com/en/tracks/web" target="_blank">codecademy</a> &amp; how to use <a href="http://www.w3schools.com/" target="_blank">w3schools</a> as reference &amp; <a href="http://validator.w3.org/#validate-by-input" target="_blank">HTML5 validator</a>
        </li>
        <li id="Week1Step12">
          Explain mentor meeting, have each student pick a time from your available time schedule
        </li>
        <li id="Week1Step13">
          Start Google project with notepad/textedit start project from folder created earlier - just need to add in HTML5 stuff html, head, body tags -  ref: <a href="http://www.w3schools.com/html/html5_intro.asp" target="_blank">HTML5 w3schools</a> - show them the link for the page to replicate - <a href="http://codifyacademy.com/google_demo/" target="_blank">Google Demo</a> - have them use inspect element to recreate what they see, and ask for help when stuck
        </li>
      </ol>
